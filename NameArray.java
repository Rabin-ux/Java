public class NameArray {
    public static void main(String[] args) {
        char[] name = {
            'R','a','b','i','n',' ',
            'R','a','j',' ',
            'C','h','a','u','d','h','a','r','y'
        };
        for (int i = 0; i < name.length; i++) {
            System.out.print(name[i]);
        }
    }
}
